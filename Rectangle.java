public class Rectangle {
    public static void main(String[] args){
        int length = 40;
        int breadth= 60;
        int area= length* breadth;
        int perimeter= 2*(length+ breadth);

        System.out.println("The perimeter of the rectangle will be: "+ perimeter);
        System.out.println("The Area of the rectangle will be: "+ area);
    }
}
